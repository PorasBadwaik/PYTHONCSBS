def add(a,b):
    sum = a + b
    return sum
result = add(5, 3)
print("The sum is:", result)

def multi(a,b):
    pro = a * b
    return pro
result = multi(5, 3)
print("The product is:", result)

def squ(a):
    squ = a * a
    return squ
result = squ(5)
print("The square is:", result)